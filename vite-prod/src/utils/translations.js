export const RTL_LANGS = ['he', 'ar'];
export const SUPPORTED_LANGS = ['he', 'ar', 'ru'];
export const LANG_LABELS = { he: 'עברית', ar: 'العربية', ru: 'Русский' };

const LANG_SUFFIX = { he: 'He', ar: 'Ar', ru: 'Ru', en: 'En' };
export function lf(obj, field, lang) {
  if (!obj) return '';
  const s = LANG_SUFFIX[lang] || 'En';
  return obj[field + s] || obj[field + 'En'] || obj[field] || '';
}

export function t(key, lang = 'he') {
  return translations[lang]?.[key] || translations.he[key] || key;
}

export function tReplace(key, lang, vars = {}) {
  let str = t(key, lang);
  for (const [k, v] of Object.entries(vars)) {
    str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
  }
  return str;
}

