// Flatten all scenarios for quick lookup
export function getAllScenarios() {
  const all = [];
  for (const industry of Object.keys(SCENARIOS)) {
    for (const level of Object.keys(SCENARIOS[industry])) {
      for (const scenario of SCENARIOS[industry][level]) {
        all.push({ ...scenario, industry, careerLevel: level });
      }
    }
  }
  return all;
}

export function getScenarioById(id) {
  return getAllScenarios().find(s => s.id === id) || null;
}
